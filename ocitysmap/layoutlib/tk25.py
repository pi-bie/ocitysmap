    @staticmethod
    def _sheet2latlng(sheetno):
        yy = sheetno // 100
        xx = sheetno % 100

        lat = (56*60 - yy*6) / 60
        lon = (xx*10 - (5*60 + 40)) / 60

    @staticmethod
    def _latlng2sheet(lat, lon):
        xx = int((lon * 60 - (5*60 + 40)) / 10)
        yy = int((56*60 - lat*60) / 6)

        if yy < 1 or yy > 99:
            return None
        if xx < 0 or xx > 99:
            return None

        sheetno = yy * 100 + xx
        if sheetno not in tk25_names:
            return None

        return sheetno

    @staticmethod
    def _sheet2name(sheetno):
        if sheetno in tk25_names:
            return tk25_names[sheetno]
        return none

